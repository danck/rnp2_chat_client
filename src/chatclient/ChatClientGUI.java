    public ChatClientGUI() {
        String[] user = new String[2];
        //Using a JPanel as the message for the JOptionPane
        JPanel userPanel = new JPanel();
        userPanel.setLayout(new GridLayout(2, 2));

        //Labels for the textfield components        
        JLabel usernameLbl = new JLabel("Username:");
        JLabel serverIPLbl = new JLabel("Server IP:");

        JTextField usernameFld = new JTextField();
        JTextField serverIPFld = new JTextField();

        //Add the components to the JPanel        
        userPanel.add(usernameLbl);
        userPanel.add(usernameFld);
        userPanel.add(serverIPLbl);
        userPanel.add(serverIPFld);
        
        //As the JOptionPane accepts an object as the message
        //it allows us to use any component we like - in this case 
        //a JPanel containing the dialog components
        boolean connected = false;
        while (!connected) {
            int input = JOptionPane.showConfirmDialog(null, userPanel, "Username and chatroom",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

            // Close dialog and exit program when initial dialog is aborted
            if ((input == JOptionPane.OK_CANCEL_OPTION)
                    | input == JOptionPane.CLOSED_OPTION){
                System.exit(1);
            }
            
            //Retrieve session data
            nickname = usernameFld.getText();
            String server = serverIPFld.getText();
            
            // TODO check username compliance

            
            try {
                ssocket.connect(new InetSocketAddress(server, 50000), 5000);

                connected = true;
            } catch (IOException e) {
                System.out.println(server + " not responding");
            }
        }
        initComponents();
        jLabel3.setText(nickname);
        jLabel5.setText(ssocket.getInetAddress().toString());
        jTextField1.requestFocus();
        try {
            this.udpSock = new DatagramSocket(50001);
        } catch (SocketException ex) {
            Logger.getLogger(ChatClientGUI.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
        
        // initialize helpers
        updater     = new tUpdater(ssocket, this);
        receiver    = new tReceiver(udpSock, this);

        // start helpers
        updater.start();
        receiver.start();

        
    }

