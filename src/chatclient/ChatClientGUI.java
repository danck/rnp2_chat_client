    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        List<String> participants = Backend.getParticipantIPs();
        for (String host : participants){
            try {
                InetAddress IP = InetAddress.getByName(host);
                String msg = nickname + ": " + jTextField1.getText();
                byte[] toSend = msg.getBytes().clone();
                //jTextField1.setText("");
                DatagramPacket output = new DatagramPacket(toSend, toSend.length, IP, UDP_PORT);
                try {
                    udpSock.send(output);
                } catch (IOException ex) {
                    Logger.getLogger(ChatClientGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (UnknownHostException ex) {
                Logger.getLogger(ChatClientGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

